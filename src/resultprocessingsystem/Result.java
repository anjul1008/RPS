     void myinit()
    {
        this.setSize(DBConnection.getScreenSize().width, DBConnection.getScreenSize().height - 30);
        setExtendedState(MAXIMIZED_BOTH);
         try{
                Statement st= con.createStatement();
                rs = st.executeQuery("Select * From Exam Where EnrollNo ='"+UserLogin.Enroll+"'");
                if(rs.next())
                {
                    jLabel6.setText(rs.getString(8));
                    jLabel3.setText(rs.getString(10));
                    jLabel9.setText(rs.getString(9));
                    
                }
                                
            }catch(Exception e)
            {
                e.printStackTrace();
            }
         try{
             
             Statement st1 = con.createStatement();
             ResultSet rs1 = st1.executeQuery("Select * From Exam Where EnrollNo='"+UserLogin.Enroll+"'");
             rs1.next();
             int ctr =0;
             while(!rs1.isAfterLast()){
                Crs[ctr]= rs1.getString(3);
                ctr++;
                rs1.next();
             }
             ResultSet rs2[] = new ResultSet[ctr];
             for(int i =0;i<ctr;i++){
                rs2[i] = st1.executeQuery("Select * From schemecrs Where SchemeCrsId='"+Crs[i]+"'");
                rs2[i].next();
                for(int j=0;j<8;j++)
                {
                    if(j==0)
                        jTable1.getModel().setValueAt(i+1,i,j);
                    if(j==1)
                        jTable1.getModel().setValueAt(rs2[i].getString(2), i, j);
                    if(j==2)
                        jTable1.getModel().setValueAt(rs2[i].getString(3), i, j);
                    if(j==3)
                        jTable1.getModel().setValueAt(rs2[i].getString(5), i, j);
                    if(j==5)
                        jTable1.getModel().setValueAt(rs2[i].getString(6), i, j);
                    if(j==7)
                        jTable1.getModel().setValueAt(rs2[i].getString(7), i, j);
                }
                
               
             }
             int total = 0;
             Statement st2 = con.createStatement();
             ResultSet rs3 = st2.executeQuery("Select * From Exam Where EnrollNo='"+UserLogin.Enroll+"'");
             rs3.next();
             for(int i=0;i<=10;i++)
                {
                    for(int j=0;j<=2;j++)
                    {
                        if(j==0)
                        {
                            jTable1.getModel().setValueAt(rs3.getInt(5), i,j+4);
                            total+=rs3.getInt(5);
                        }
                        if(j==1)
                        {
                            jTable1.getModel().setValueAt(rs3.getInt(6), i,j+5);
                            total+=rs3.getInt(6);
                        }
                        if(j==2)
                        {
                            jTable1.getModel().setValueAt(rs3.getInt(7), i,j+6);
                            total+=rs3.getInt(7);
                        }
                        
                    }
                    jTable1.getModel().setValueAt(total, i, 9);
                    jTable1.getModel().setValueAt(rs3.getString(11),i,10);
                    total=0;
                    if(rs3.isLast())
                        break;
                    rs3.next();
                }
         }catch(Exception e)
         {
             e.printStackTrace();
         }
    }    

